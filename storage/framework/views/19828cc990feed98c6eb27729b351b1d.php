<div class="container mx-auto p-8 max-w-3xl">
    <h1 class="text-3xl font-bold mb-6 text-center text-[#E2001A]">L’Entente Urbaine de Football de Lubumbashi (EUFLU)</h1>

    <h2 class="text-xl font-semibold mt-8 mb-2">Qui sommes-nous ?</h2>
    <p class="mb-4">L'Entente Urbaine de Football de Lubumbashi (EUFLU) est une organisation sportive basée à Lubumbashi, en République Démocratique du Congo. Affiliée à la FECOFA, elle a pour mission de promouvoir, organiser et encadrer le football urbain, en particulier au niveau amateur et semi-professionnel. L’EUFLU D1, première division de l’entité, développe les jeunes talents, renforce la discipline sportive et valorise le football local à travers des championnats structurés et accessibles.</p>

    <h2 class="text-xl font-semibold mt-8 mb-2">Notre histoire</h2>
    <p class="mb-4">Fondée en 1947, l’EUFLU est l’une des plus anciennes structures sportives de Lubumbashi. Depuis 2012, avec la structuration en divisions et groupes, elle a renforcé son rôle dans la gestion des compétitions locales et le développement du football de jeunes talents. L’EUFLU fait partie intégrante de la FECOFA et est placée sous la supervision de la Ligue de Football du Katanga (LIFKAT).</p>

    <h2 class="text-xl font-semibold mt-8 mb-2">Où nous trouver ?</h2>
    <p class="mb-4">L’EUFLU est implantée au Stade Omnisports Frédéric Kibassa-Maliba, dans la commune de Kenya à Lubumbashi. Ce complexe sportif, accessible par plusieurs grandes avenues (Digue, Basilique, Moba, Imaïté), accueille les équipes, officiels et supporters lors des activités sportives organisées par l’EUFLU.</p>

    <h2 class="text-xl font-semibold mt-8 mb-2">Notre mission</h2>
    <p class="mb-4">L’EUFLU organise, gère et promeut les compétitions de football urbain, notamment en Division 1. Elle valorise les talents locaux, encadre les clubs affiliés et améliore le niveau compétitif du football à Lubumbashi. L’EUFLU encourage la pratique du sport chez les jeunes, l’esprit de fair-play et contribue au rayonnement du football congolais.</p>

    <h2 class="text-xl font-semibold mt-8 mb-2">Nos championnats</h2>
    <ul class="list-disc ml-8 mb-4">
        <li><b>Division 1 (D1)</b> : Deux pools de 14 équipes. Les trois premiers de chaque pool se qualifient pour les play-offs. Ce championnat est organisé sous la supervision de la LIFKAT.</li>
        <li><b>Division 2 (D2)</b> : Plusieurs équipes réparties en pools, servant de vivier pour la montée en D1.</li>
    </ul>
    <p class="mb-2">Chaque saison, les meilleurs clubs de D2 accèdent à la D1 via les play-offs de promotion, tandis que certaines équipes de D1 sont reléguées en D2 selon leurs résultats.</p>

    <h2 class="text-xl font-semibold mt-8 mb-2">Comment fonctionne l’EUFLU ?</h2>
    <p class="mb-2">L’EUFLU est dirigée par un comité exécutif élu (président, secrétaire, trésorier, responsables techniques). Elle assure :</p>
    <ul class="list-disc ml-8 mb-4">
        <li>La planification et la supervision des championnats D1 et D2</li>
        <li>L’affiliation des clubs, la validation des licences, la gestion disciplinaire</li>
        <li>Le suivi technique (arbitres, feuilles de match, résultats, classements)</li>
        <li>Le développement du football local et la formation des jeunes</li>
    </ul>
    <p class="mb-4">L’EUFLU agit en conformité avec la LIFKAT et la FECOFA, en collaboration avec les clubs affiliés, pour garantir l’équité, la transparence et la compétitivité des compétitions.</p>

    <h2 class="text-xl font-semibold mt-8 mb-2">Frais de participation</h2>
    <p class="mb-4">La participation aux championnats EUFLU est conditionnée par le paiement de frais fixés chaque saison par l’Assemblée Générale. Ces frais couvrent l’organisation, la logistique, l’arbitrage et l’administration. Une circulaire officielle informe chaque année les clubs des tarifs, échéances et conséquences en cas de non-paiement, pour garantir une gestion transparente et équilibrée des ressources.</p>

